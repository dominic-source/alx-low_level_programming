/**
 * op_add - add two integers
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtract two numbers
 * @a: first number
 * @b: second number
 * Return: difference
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: first number
 * @b: second number
 * Return: product
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: first number
 * @b: second number
 * Return: division
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - remainder of a division
 * @a: first number
 * @b: second number
 * Return: modulus
 */
int op_mod(int a, int b)
{
return (a % b);
}

